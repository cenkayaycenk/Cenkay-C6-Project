package Day02_Variables;

public class c3_EscapeSequence {
    public static void main(String[] args) {

        // \t it is going to leave paragraph space on the console

        System.out.println("\t this is a tab space");

        // \n it is going to move on new line on the console

        System.out.print("\nCenkay");

        // \" this is double quote
        System.out.println("\nThis is \"Cenkay\"");

        // \\ prints \ on the console
        System.out.println("\nThis is back slash \\");




    }
}
